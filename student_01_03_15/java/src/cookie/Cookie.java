package cookie;

public class Cookie 
{
	private String username;
	private String password;
	private int userID;
	private int gameID;
	
	Cookie()
	{
		
	}

	/**
	 * @return the username
	 */
	public String getUsername() 
	{
		return username;
	}

	/**
	 * @return the password
	 */
	public String getPassword()
	{
		return password;
	}

	/**
	 * @return the userID
	 */
	public int getUserID() 
	{
		return userID;
	}

	/**
	 * @return the gameID
	 */
	public int getGameID() 
	{
		return gameID;
	}
}
